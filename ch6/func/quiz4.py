# 두 수를 입력 받아 첫번째 수를 두번째 수로 나누고
# 몫을 출력하는 함수 만들기

# 나누는 수가 0이면 "나누는 수는 0이 될 수 없습니다"를 출력하고 함수를 종료하세요
# 입력값을 검사하면 함수가 정상적으로 종료됨
def divide(n1, n2) :
    if n2 == 0:
        print("나누는 수는 0이 될 수 없습니다")
        # 아래있는 계산식을 만나지 않도록 함수를 여기서 종료
        return
    print(n1//n2)

# 함수 호출
divide(10,2)
divide(10,0) # 나누는 수를 0으로 하면 에러남

