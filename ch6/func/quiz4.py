# 두 수를 더해서 합을 구하는 함수
# 식 자체는 성립되나 의도한대로 계산되지 않음
# 입력값이 숫자가 아니면 더하기를 수행할 필요가 없으므로 중간에 함수를 종료할 것
def add(n1, n2):
    if type(n1)!= int or type(n2)!=int :
        print("입력받은 값이 숫자가 아닙니다")
        return
    
    print(f'{n1} + {n2} = {n1+n2}')

# 함수 호출
add(3,4)
add(10,20)
add('aa',5)
add('aa','bb') #문자를 입력하면 더하기 x

