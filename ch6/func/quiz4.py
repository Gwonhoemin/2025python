# 나이를 입력받아 성인 여부를 출력하는 함수를 작성
# 단 나이가 0보다 작으면 "잘못된 입력입니다" 출력하고 함수 종료
def func(age):

    if age < 0:
        print("잘못된 입력입니다")
        return # 함수 강제 종료

    if age >= 20:
        print("성인입니다")
    else:
        print("성인이 아닙니다")

func(20)
func(8)
func(-8)

# 사람의 정보를 출력하는 함수 만들기
# 매개변수: 이름, 나이, 성별
# 성별의 초기값 설정. 기본값 m
# 초기값이 있는 매개변수는 항상 맨뒤에 배치해야 함
def info(name,  age, gender='m') :
    print(f'나의 이름은 {name}입니다')
    print(f'나이는 {age}살입니다')
    if gender == 'm':
        print('남자입니다')
    else:
        print('여자입니다')

# 함수 호출
# 도우너는 gender의 초기값을 사용해서 '남자'
info('도우너', 8)
# 둘리는 입력한 값 그대로 '여자'
info('둘리', 10, 'f')